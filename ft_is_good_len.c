#include "libft_tetri.h"

int		ft_is_good_len(int len, int *n)
{
	if (n == NULL || len <= 0)
		return (0);
	while ((20 * (*n)) + (1 * ((*n) - 1)) < len)
		(*n)++;
	if ((*n) > 26)
		return (0);
	return (((20 * (*n)) + (1 * ((*n) - 1)) == len) ? 1 : 0);
}
