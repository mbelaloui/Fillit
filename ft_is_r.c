#include "libft_tetri.h"

int		ft_is_r(char **tetrim_mat, int x, int y)
{
	if (tetrim_mat == NULL || x < 0 || x > 3 || y < 0 || y >= 3)
		return (0);
	return ((tetrim_mat[x][y + 1] == '#') ? 1 : 0);
}
