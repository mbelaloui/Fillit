#include "fillit_p_1.h"

int		ft_is_file_good_len(int len, int *n)
{
	if (len <= 0 || *n <= 0 || *n > 26)
		return (0);
	while ((20 * (*n)) + (1 * ((*n) - 1)) < len)
		(*n)++;
	return (((20 * (*n)) + (1 * ((*n) - 1)) == len) ? 1 : 0);
}
