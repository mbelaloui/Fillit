#include "fillit_p_1.h"

int		ft_is_d(char **tetri_mat, int x, int y)
{
	if (tetri_mat == NULL || x < 0 || x >= 3 || y < 0 || y > 3)
		return (0);
	return ((tetri_mat[x + 1][y] == '#') ? 1 : 0);
}
